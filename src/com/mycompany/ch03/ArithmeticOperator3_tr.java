package com.mycompany.ch03;

public class ArithmeticOperator3_tr {

	public static void main(String[] args) {

		int val1 = 7;
		int val2 = 3;
		
		int result1 = val1 + val2;
		int result2 = val1 - val2;
		int result3 = val1 * val2;
		int result4 = val1 / val2;
		int result5 = val1 % val2;
		
		System.out.println(result1); // 10
		System.out.println(result2); // 4
		System.out.println(result3); // 21
		System.out.println(result4); // 2
		System.out.println(result5); // 1
		
	}

}
