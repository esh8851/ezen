package com.mycompany.ch06.third;

public class Arithmetic {
	
//	사칙연산 함수 생성(+,-,*,/,%)
	
	public int sum (int a, int b) {
		int result = a + b;
		return result;
	}
	
	public int minus (int a, int b) {
		int result = a - b;
		return result;
	}
	
	public int multiply (int a, int b) {
		int result = a * b;
		return result;
	}
	
	public int divide (int a, int b) {
		int result = a / b;
		return result;
	}
	
	public int remain (int a, int b) {
		int result = a % b;
		return result;
	}

}
